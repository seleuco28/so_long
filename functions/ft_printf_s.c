#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	c;

	c = 0;
	if (!s)
	{
		return (write(1, "(null)", 6));
	}
	while (s[c] != '\0')
	{
		ft_putchar(s[c]);
		c++;
	}
	return (c);
}
